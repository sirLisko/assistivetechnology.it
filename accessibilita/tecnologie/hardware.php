		<h3>Scudi</h3>
		<p>Gli scudi, anche chiamati “copri tastiera”, sono dei dispositivi rimovibili che consentono, a chi ha problemi di controllo “fine” de movimento delle mani, di digitare con maggior cura e conseguentemente con un minor numero di errori di battitura. Per far questo lʼutente può appoggiare lʼintera mano sulla superficie dello scudo posizionato sulla sommità della tastiera e, infilando le dita in corrispondenza dei tasti desiderati, può digitare quello che desidera prevenendo battiture involontarie. Solitamente sono di colore scuro, opposto a quello della tastiera, in modo da facilitare ulteriormente il riconoscimento dei tasti, in quanto li “incornicia”.</p>
