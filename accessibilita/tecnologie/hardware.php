		<h2>2.2.1.3 Sensori</h2>
		<p>Sono dispositivi di tipo on/off che consentono di catturare in modo efficace e al tempo stesso più semplice possibile i movimenti volontari di un disabile motorio, anche grave. Funzionano come interruttori e, se configurati adeguatamente, consentono di controllare dispositivi hardware e/o software in modo tale da poter essere una valida alternativa ai canonici dispositivi di input.</p>
		<h3>Sensori a pressione</h3>
		<p>Sono i più semplici e comuni, la funzione di input si basa sulla pressione appunto della superficie del sensore. Possono essercene di vari tipi in base alle esigenze ed ai problemi degli utenti che devono utilizzarli. Oltre al modello base (<em>fig. 15a</em>), caratterizzato da una superficie piana solitamente attivabile grazie alla pressione della mano, si possono trovare <em>sensori da piede</em> (<em>fig. 15b</em>), caratterizzati da una notevole robustezza; sensori da viso, al contrario di quelli da piede sono caratterizzati da una superficie morbida e un alto livello di sensibilità; sensori da mano (<em>fig. 15c</em>), molto piccoli e caratterizzati da la presenza di più tasti da selezionare con lʼuso delle dita.</p>
		<p>Solitamente questo tipo di sensori ha la possibilità di regolare la forza da applicare per l'attivazione. Inoltre, in alcuni modelli, eʼ presente un sistema di feedback che può essere sia tattile che sonoro.</p>
