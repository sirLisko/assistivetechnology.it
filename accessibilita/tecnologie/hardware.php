		<h3>Touch-screen</h3>
		<em>Sono tipologie di monitor con integrato un sistema che rende l'area del video sensibile al tocco, trasformando così il computer in un sistema a tocco integrato. Permettono di emulare le funzioni del mouse e gestire i programmi attraverso il semplice tocco di un dito o una stilo sullo schermo. Questa modalità di puntamento è molto intuitiva e diretta ed eʼ adatta per gli utenti che hanno difficoltà ad interfacciarsi con i tradizionali sistemi di puntamento. I monitor touch-screen consentono di effettuare tutte le operazioni possibili con un mouse tradizionale come il doppio click, il click destro e il trascinamento. Possono inoltre essere utilizzati come tavoletta grafica oppure anche come tastiera a schermo. Inoltre l'innovativa tecnologia detta "multi-touch" che consente, come dice la parola stessa, la gestione di più tocchi contemporaneamente, apre tutto un nuovo modo di pensare questi touch-screen, aumentando di gran lunga le azioni possibili.</em>
