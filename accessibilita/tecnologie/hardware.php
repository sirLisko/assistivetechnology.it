		<h2>2.2.1.2 Mouse</h2>
		<p>Il mouse è un dispositivo in grado di inviare un input ad un computer in modo tale che ad un suo movimento ne corrisponda uno analogo di un indicatore sullo schermo detto puntatore. È inoltre dotato di uno o più tasti ai quali possono essere assegnate varie funzioni. Conseguentemente i problemi legati a questo genere di dispositivi sono esclusivamente di natura motoria. In questo caso lʼAT si preoccupa di realizzare dispositivi per coloro che non riescono a gestire il cursore, o i tasti funzione, tramite un mouse tradizionale. Esistono principalmente due sotto categorie, i così detti sistemi di puntamento alternativo oppure gli emulatori di mouse per disabilità più gravi.</p>
		<p>Esistono parecchi dispositivi atti a sostituire la figura del classico mouse per interagire con un computer. Di seguito verrano analizzati solo i dispositivi principali e non tutte le varie sottoclassi disponibili.</p>
		<h3>Trackpad</h3>
		<p>Il trackpad, a differenza della trackball, gestisce il movimento del cursore mediante il movimento di uno o più dita sulla sua superficie.</p>
		<p>Comunemente utilizzati sui computer portatile possono essere anche installati su computer desktop. Il movimento necessario per lo spostamento del cursore può essere limitato al solo lavoro di un dito della mano, questa funzione risulta notevolmente di aiuto a chi affetto da problemi di origine motoria.</p>
		<p>Anche i trackpad chiaramente sono muniti di tasti, anche addizionali, e personalizzabili.</p>
