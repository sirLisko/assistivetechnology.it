		<p>Come per gli emulatori di mouse a soffio, questo genere di sensori funziona mediante le capacità respiratorie dellʼutente. Sono attivabili sia soffiando che aspirando.</p>
		<h3>Sensore a trazione</h3>
		<p>Sensori estremamente sensibili e generalmente di piccole dimensioni. Per il loro funzionamento è sufficiente tirare una corda legata alla loro estremità, lo scorrere della corda allʼinterno del sensore ne permette la sua attivazione.</p>
		<h3>2.2.1.4 Display Braille<sup id="cite_ref_7"><a href="#cite_note_7">[7]</a></sup></h3>
		<p>Sono dispositivi elettromeccanici che permettono di trasformare l'output di un computer in testo Braille leggibile da persone non vedenti. Il funzionamento è molto semplice, vengono sollevati ed abbassati sequenze di punti corrispondenti alle combinazioni puntiformi, in modo da formare i caratteri Braille corrispondenti.</p>
		<p>Solitamente queste barre Braille sono composte da 40 celle, che corrispondono al numero di caratteri che generalmente si può trovare su ogni pagina dei libri stampati in braille su carta, anche se ne possono trovare anche da 80. Ognuna di queste celle è a sua volta formata da 8 punti, che in questo modo permettono di rappresentare 256 combinazioni, corrispondenti alla codifica <em>ASCII</em><sup id="cite_ref_8"><a href="#cite_note_8">[8]</a></sup> estesa. Solitamente una cella Braille è composta da 6 punti, il passaggio a 8 punti non comporta nessuna sostanziale variazione al modo classico di rappresentare le lettere, e di conseguenza di leggerle. L'aggiunta di 2 nuovi punti avviene sotto i 6 punti normali, in modo che non modifica il Braille classico, ma permettendo al tempo stesso la codifica di tutti i caratteri ASCII. Questa aggiunta è stato fatta con fine di arricchire il linguaggio Braille e non modificarlo.</p>
