		<h1><span class="capitolo">Capitolo 2: Accessibilita' - Tecnologia</span>2.2.1 Hardware</h1>
		<p>Esistono varie tipologie di dispositivi Assistive Technology, chiaramente differenziati in base a quale lacuna funzionale vanno a colmare nellʼinterazione di una persona diversamente abile con un computer. Chiaramente questi dispositivi possono essere combinati lʼuno con lʼaltro per aumentarne le funzionalità oppure nel caso una persona soffrisse di più disabilità.</p>
		<h2>2.2.1.1 Tastiera</h2>
		<p>La tastiera è un dispositivo che permette lʼinserimento di dati allʼinterno di un computer mediante la pressione di uno o più tasti. Conseguentemente i problemi che si possono incontrare ad interfacciarsi con questo tipo di hardware sono legati alla vista e al movimento. Per una persona ipovedente può risultare difficile vedere o distinguere i vari tasti, mentre per una persona con difficoltà di movimento può risultare difficoltoso premere solamente i tasti desiderati. Per risolvere questo genere di problemi sono stati creati numerosi dispositivi che si prefiggono di risolvere sostituendo o riadattando le normali tastiere.</p>
		<h3>Tastiere XXL</h3>
		<p>Questo genere di tastiere è adatto a persone che soffrono di disturbi visivi. Sostanzialmente si tratta di una normale tastiera con la sola differenza che le lettere, stampate sui singoli tasti, ne occupano lʼintera superficie, migliorandone decisamente il riconoscimento visivo.</p>
